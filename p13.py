# Sets

set1 = {1, 2, 3, 4, 5}
print(set1) # prints the set

# adding an element to the set
set1.add(6)
print(set1) # prints the set

# removing an element from the set
set1.remove(6)
print(set1) # prints the set

set1.pop() # removes a random element from the set

print(set1) # prints the set

set2 = {4, 5, 6, 7, 8}
# union of two sets

set3 = set1.union(set2)
print(set3) # prints the union of two sets

# intersection of two sets

set4 = set1.intersection(set2)
print(set4) # prints the intersection of two sets




