# for loop using range function
# syntax : for element in rangle (start, stop,step)

print("CASE : 1 ")
for el in range(5):
    print(el)
    
    
print("CASE : 2")
for el in range(1,5):
    print(el)
    
    
print("CASE : 3 ")
for el in range(1,5,2):
    print(el)
    
    
for i in range(1,11):
    print(i)

